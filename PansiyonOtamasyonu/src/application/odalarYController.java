    @FXML
    void changeButtonColor(ActionEvent event) {
        ToggleButton selectedButton = (ToggleButton) event.getSource(); // Seçili butonu alın
        
        if (selectedButton.isSelected()) {
            selectedButton.setStyle("-fx-background-color: red;"); // Butonun rengini kırmızı yapın
        } else {
            selectedButton.setStyle("-fx-background-color: green;"); // Butonun rengini yeşil yapın
        }
    }



    @FXML
    void initialize() {
        ToggleButton[] buttons = {btn_s1, btn_s2, btn_s3, btn_s4, btn_s5, btn_s6, btn_s7, btn_s8, btn_s9, btn_s10, btn_s11, btn_s12, btn_s13, btn_s14, btn_s15};
        
        for (ToggleButton button : buttons) {
            button.setOnAction(this::changeButtonColor); // Butonların tıklama olaylarına aynı olay dinleyiciyi bağlayın
            button.setStyle("-fx-background-color: green;"); // Butonların başlangıç rengini yeşil yapın
        }

        anchor_ilk.sceneProperty().addListener((obs, oldScene, newScene) -> {
            if (oldScene == null && newScene != null) {
                // İlk defa sahne yükleniyor, herhangi bir panele geçiş yapılmadı
                for (ToggleButton button : buttons) {
                    button.setSelected(false); // Başlangıçta butonların seçili olmadığından emin olun
                    button.setStyle("-fx-background-color: green;"); // Butonların rengini değiştirmeyin
                }
            } else if (oldScene != null && newScene == null) {
                // Sahne kapatılıyor, panele geri dönülüyor
                for (ToggleButton button : buttons) {
                    button.setSelected(true); // Butonların seçili olduğunu belirtin
                    button.setStyle("-fx-background-color: red;"); // Butonların rengini değiştirmeyin
                }
            }
        });
    }
